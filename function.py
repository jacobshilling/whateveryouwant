def number_grade(letter_grade):

    if letter_grade == "A":
        print ("100 to 91")
    elif letter_grade == "B":
        print ("90 to 81")
    elif letter_grade == "C":
        print ("80 to 71")
    elif letter_grade == "D":
        print ("70 to 61")
    elif letter_grade == "F":
        print ("60 to 0")

number_grade("B")
